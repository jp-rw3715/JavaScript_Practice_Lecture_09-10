{
  // 1. console.log() - Console में message दिखाता है
  console.log("Hello World!");

  // 2. alert() - Alert box में message दिखाता है
  alert("Hello World!");

  // 3. document.write() - Page पर सीधे message लिखता है
  document.write("Hello World! (from document.write)<br>");

  // 4. innerHTML - HTML element के अंदर content update करता है
  document.getElementById("demo").innerHTML = "Hello World! (using innerHTML)";
}
